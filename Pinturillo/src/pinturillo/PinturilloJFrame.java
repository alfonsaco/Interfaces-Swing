    // PINTAR EN EL CONTENEDOR
    private void ContenedorPintarMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ContenedorPintarMouseDragged
        Graphics graphic=ContenedorPintar.getGraphics();
        graphic.setColor(c);
        graphic.fillOval(evt.getX()-GrosorPincel.getValue()/2, evt.getY()-GrosorPincel.getValue()/2, GrosorPincel.getValue(), GrosorPincel.getValue());
        
        ContenedorPintar.setCursor(Cursor.getPredefinedCursor(Cursor.CROSSHAIR_CURSOR));
    }//GEN-LAST:event_ContenedorPintarMouseDragged

