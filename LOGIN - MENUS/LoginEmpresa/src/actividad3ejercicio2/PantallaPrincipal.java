    // PLACEHOLDER PARA EL TEXT FIELD DE USUARIO
    private void jTextFieldUsuarioFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldUsuarioFocusGained
        String texto=jTextFieldUsuario.getText();
        if(texto.equals("Insertar usuario...")) {
            jTextFieldUsuario.setText("");
            jTextFieldUsuario.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_jTextFieldUsuarioFocusGained

    private void jTextFieldUsuarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldUsuarioFocusLost
        String texto=jTextFieldUsuario.getText();
        if(texto.equals("")) {
            jTextFieldUsuario.setText("Insertar usuario...");
            jTextFieldUsuario.setForeground(new Color(153,153,153));
        }
    }//GEN-LAST:event_jTextFieldUsuarioFocusLost

    public boolean validarFomulario() {
        String usu=jTextFieldUsuario.getText();
        String contra=jPasswordFieldContra.getText();
        
        if(!usu.equals("") && !contra.equals("") && !usu.equals("Insertar usuario...") && !contra.equals("jPasswordField1")) {
            JOptionPane.showMessageDialog(this, "Sesión iniciada con éxito");
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Datos incorrectos");
            return false;
        }
    }
    // CLICK Y VALIDAR INICIO DE SESIÓN
    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        validarFomulario();
    }//GEN-LAST:event_jLabel5MouseClicked

